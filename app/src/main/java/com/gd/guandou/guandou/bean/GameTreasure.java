public class GameTreasure {

    private String text;
    private String recource;
    private String createDate;
    private String jfNum;
    private String type;
    private String gdNum;

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public String getRecource() {
        return recource;
    }

    public void setRecource(String recource) {
        this.recource = recource;
    }

    public String getCreateDate() {
        return createDate;
    }

    public void setCreateDate(String createDate) {
        this.createDate = createDate;
    }

    public String getJfNum() {
        return jfNum;
    }

    public void setJfNum(String jfNum) {
        this.jfNum = jfNum;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getGdNum() {
        return gdNum;
    }

    public void setGdNum(String gdNum) {
        this.gdNum = gdNum;
    }


}
